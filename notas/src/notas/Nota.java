package notas;

public class Nota {
	private double valor;
	private String data;
	
	
	public double getValue() {
		return valor;
	}
	public void setValue(double value) {
		this.valor = value;
	}
	public String getDate() {
		return data;
	}
	public void setDate(String date) {
		this.data = date;
	}
	
	
}
